package com.tiantiantao.pojo;

public class Type {

    private String typeId;
    private String typename;

    public String getId() {
        return typeId;
    }

    public void setId(String typeId) {
        this.typeId = typeId;
    }

    public String getTypename() {
        return typename;
    }

    public void setTypename(String typename) {
        this.typename = typename;
    }
}
